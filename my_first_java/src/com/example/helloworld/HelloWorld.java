package com.example.helloworld;

public class HelloWorld {
    public static void main(String[] args) {
        // 1定义一个单精度浮点类型变量
        float f = 1234.328f;
        System.out.println("f=" + f);
        // 2定义一个双精度浮点类型变量
        double d = 52.454;
        System.out.println("d=" + d);
        // 3 将整型赋值给浮点型
        int i = 10;
        System.out.println("int=" + i);
        // 4 变量间的赋值
        double d2 = d;
        System.out.println("d2=" + d2);
    }
}
